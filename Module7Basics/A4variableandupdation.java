package Module7Basics;

public class A4variableandupdation {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x);
        x = 8;
        System.out.println(x);
        x = x+2;
        System.out.println(x);
        x = x-3;
        System.out.println(x);
        x = x*3;
        System.out.println(x);
    }
}
