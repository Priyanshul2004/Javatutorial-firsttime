package Module12Array;

public class A3basic {
    public static void main(String[] args) {
        int[] array = {2,3,4,5,6,7};
        for(int i=0; i<=5; i++){
            System.out.print(array[i]+" ");
        }
        System.out.println();
        for(int i=0; i<=array.length-1; i++){
            System.out.print(array[i]+" ");
        }
    }
}
