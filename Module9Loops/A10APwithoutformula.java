package Module9Loops;
import java.util.*;

public class A10APwithoutformula {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int a = 4;
        for(int i=0; i<=n ;i++){
              System.out.println(a);
              int d = 3;
              a += d; 
        }
    }
}
